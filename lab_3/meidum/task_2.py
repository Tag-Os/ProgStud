def count_zeros_in_octal():
    """
    Вычисляет выражение 7 * (512 ** 120) - 6 * (64 ** 100) + (8 ** 210) - 255
    и возвращает количество нулей в его восьмеричной записи.

    Проверка на простом примере:
    >>> n = 8  # 8 в восьмеричной = 10, один ноль
    >>> oct(n)[2:].count('0')
    1

    Проверка на числе без нулей:
    >>> n = 7  # 7 в восьмеричной = 7, ноль = 0
    >>> oct(n)[2:].count('0')
    0

    Проверка на числе с несколькими нулями:
    >>> n = 64  # 64 в восьмеричной = 100, два нуля
    >>> oct(n)[2:].count('0')
    2

    Проверка типа возвращаемого значения:
    >>> isinstance(count_zeros_in_octal(), int)
    True
    
    Проверка, что результат положительный:
    >>> count_zeros_in_octal() > 0
    True
    """
    number = 7 * (512 ** 120) - 6 * (64 ** 100) + (8 ** 210) - 255
    octal_number = oct(number)[2:]
    return octal_number.count("0")


